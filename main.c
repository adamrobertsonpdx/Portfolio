/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	greenLedControl(1);//green light means ready

	redLedControl(0);//read means stop

	yellowLedControl(0);//yellow means error

	printf("green led has been turned on, yellow off and red on \n");

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  if(OV5640_Init(&ov56400bj,OV5640_R800x480,OV5640_JPEG)!=0){//initialize the camera
	  printf("camera initialization error \n");
	  Error_Handler();//initialization error handler
  }

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DCMI_Init();
  MX_USART3_UART_Init();
  MX_JPEG_Init();
  MX_USB_OTG_HS_USB_Init();
  MX_I2C1_Init();
  MX_UART5_Init();
  MX_LIBJPEG_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */

  printf("camera has been initialized \n");
  HAL_Delay(100);//delay to allow for initalization of hardware to happen

  if(HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1)!=HAL_OK){//24MHz square wave for XC
	  printf("timer error \n");
	  Error_Handler();
  }
  printf("PWM has been started \n");

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  while (1)
  {
	if(HAL_GPIO_ReadPin(BUTTON_GPIO_Port, BUTTON_Pin)){//if button is pressed, take picture
		printf("button has been pressed \n");
		greenLedControl(0);
		printf("green led is off \n");
		redLedControl(1);
		printf("red led is on \n");

		if(HAL_DCMI_Start_DMA(&hdcmi,DCMI_MODE_SNAPSHOT,(uint32_t)image_buffer,IMAGE_BUFFER_SIZE/4)!=0){
			printf("dcmi error \n");
			Error_Handler();
		}

		/*A few things about the above function, first the function is called even though it is being checked for an error
		 * Second, it says start dma, however no dma has been defined, so I think I have data going to an array
		 * Due to snapshot mode, no dcmi stop should be required
		 */
		printf("dcmi has been started(while loop) \n");
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

